StrToInt proc
    xor rax, rax
    xor r8, r8
_next_char:
    mov r8b, byte ptr [rcx]
    sub r8b, '0'
    cmp r8b, 9
    ja _done
    imul rax, 10
    add rax, r8
    inc rcx
    jmp _next_char
_done:
    ret
StrToInt endp

CheckWin proc
    push rdx
    push rcx
    push rax
    mov rcx, 10CF0000h ; Standard Window Style
    lea rax, CustomDLGCLASSNAME
    cmp rdx, rax
    cmove r9, rcx
    pop rax
    pop rcx
    pop rdx
    ret
CheckWin endp

